package testThreadTrans.mySleepAndWhile;

public class ThreadB extends Thread {

	private MyList list;
	
	public ThreadB(MyList list) {
		this.list = list;
	}
	
	@Override
	public void run() {
		try {
			while(true) {
				if(list.size() == 5) {
					System.out.println("==5了，线程B退出");
					throw new InterruptedException();
				}
			}
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
	}

}
